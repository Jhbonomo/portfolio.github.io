// we asign an empty template string to htmlCode. We'll add something inside afterwards.
let htmlCode = ``;
//define pages
let page = document.body.id;
let pageArray = "";

switch (page) {
  case "pnipe":
    pageArray = pnipeArray;
    break;

  case "e-commerce":
    pageArray = viaArray;
    break;
  case "museum":
    pageArray = museuArray;
    break;
  case "dlab":
    pageArray = dlabArray;
    break;
  case "icpedu":
    pageArray = icpeduArray;
    break;
}

pageArray.forEach(function (singleCard) {
  if (singleCard.pictureUrl !== null && singleCard.caption !== null) {
    htmlCode = `${htmlCode}
          <section class="section" href="#${singleCard.order}" id="section-${singleCard.order}">
          ${singleCard.title}

          <p>${singleCard.description}</p>
          <div class="content-case"><img src="${singleCard.pictureUrl}"/></div>
          <p class="caption">${singleCard.caption}</p>


            </section>
        `;
  } else {
    htmlCode = `${htmlCode}
          <section class="section" href="#${singleCard.order}" id="section-${singleCard.order}">
            ${singleCard.title}
            <p >${singleCard.description}</p>
          </section> 
        `;
  }
});

const portfolioCards = document.querySelector(".cards-list");
portfolioCards.innerHTML = htmlCode;
